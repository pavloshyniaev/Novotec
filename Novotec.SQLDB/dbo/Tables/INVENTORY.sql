    [INDATE]      DATETIME        NULL,
    [INWHO]       BIGINT          CONSTRAINT [DF_INVENTORY_INWHO] DEFAULT ((0)) NOT NULL,
    [INSTIDENT]   BIGINT          CONSTRAINT [DF_INVENTORY_INSTIDENT] DEFAULT ((0)) NOT NULL,
    [ININDATE]    DATETIME        NULL,
