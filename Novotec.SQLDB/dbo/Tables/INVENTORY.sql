    [INPBIDENT]   BIGINT          CONSTRAINT [DF_INVENTORY_INPBIDENT] DEFAULT ((0)) NOT NULL,
    [INACT]       NUMERIC (12, 3) CONSTRAINT [DF_INVENTORY_INACT] DEFAULT ((0)) NOT NULL,
    [INBUYPRICE]  NUMERIC (10, 4) CONSTRAINT [DF_INVENTORY_INBUYPRICE] DEFAULT ((0)) NOT NULL,
