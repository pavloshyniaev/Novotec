    [INCORR]      TINYINT         CONSTRAINT [DF_INVENTORY_INCORR] DEFAULT ((0)) NOT NULL,
    [INMEMO]      VARCHAR (MAX)   CONSTRAINT [DF_INVENTORY_INMEMO] DEFAULT ('') NOT NULL,
    CONSTRAINT [PK_INVENTORY] PRIMARY KEY CLUSTERED ([INIDENT] ASC)
